            <h3>六. 副词后的倒装</h3>
            <p>here 和 there 之后以及诸如 back, down, off, up 等一类的副词小品词之后，名词主语一般置于谓语之后。这类副词常与诸如 come 和 go 等转移动词一起连用。如：</p>
            <ul class="disc">
                <li>Here comes a taxi! There goes the last train! 有辆出租车来了! 最后一班火车开走了! (注意这里不可用进行时) </li>
                <li>Down came the rain and up went the umbrellas. 下雨了，伞都撑了起来。</li>
            </ul>
            <p>当我们给人东西或确定位置时，这种倒装常用在be之后(主语往往是复数)。如：</p>
            <ul class="disc">
                <li>Here’s a cup of tea for you. 给你一杯茶。(给东西) </li>
                <li>Here’s your letters. 这儿有你的信。(给与或指明)</li>
                <li>There’s (重读) Johnny smith. 约翰尼·史密斯在那儿。(确定位置) </li>
            </ul>
            <p>主语如是代词则不倒装。如：</p>
            <ul class="disc">
                <li>Here it comes. 它来了。 <br>There she goes. 她走了。</li>
                <li>Up it went. 它上去了。 <br>Here you are. 给你。</li>
                <li>There she is. 她在那儿。</li>
            </ul>
        </div>

