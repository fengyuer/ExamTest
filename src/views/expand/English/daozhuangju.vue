            <h3>三、涉及only的倒装及考题分析</h3>
            <p>按英语习惯同，当“only+状语”位于句首时，其后句子要用部分倒装。如：</p>
            <ul class="disc">
                <li>Only then did he realize that he was wrong. 到那时他才意识到他错了。</li>
                <li>Only in this way are you able to do it well. 你只有用这种方法才能把它做好。</li>
                <li>Only in this way can our honour be saved. 只有这样，才能保住我们的荣誉。</li>
                <li>Only then did I understand what she meant. 只有到那时我才明白她的意思。</li>
                <li>Only after her death was I able to appreciate her. 只有到她死后我才认识到她的价值。</li>
                <li>Only when he returned home did he realize what had happened. 当他回到家里时,才知道出了什么事。</li>
                <li>Only in this way can we learn English. 只有这样才能学会英语。</li>
                <li>The pilot reassured the passengers. Only then did I realize how dangerous the situation had been. 飞机驾驶员要乘客们放心，这时我才明白刚才的情况有多危险。</li>
                <li>Only by shouting was he able to make himself heard. 他只有叫喊才能让别人听到他。</li>
                <li>Only when we landed did we see how badly the plane had been damaged. 我们只是在着陆之后才看到飞机损坏的严重程度。</li>
                <li>Only on one point do I agree with you. 只有一点，我同意你的说法。</li>
