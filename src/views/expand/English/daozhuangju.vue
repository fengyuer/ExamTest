            <h3>七. 地点状语后的倒装</h3>
            <p>地点状语后面如有表示位置的动词 (如lie, live, sit, stand) 或转移的动词 (如come, go, rise)，用作主语的名词可以放在动词之后。这种情形主要出现在描写文中。如：</p>
            <ul class="disc">
                <li>At the top of the hill stood the tiny chapel. 那座小教堂矗立在山顶上。</li>
                <li>In the fields of poppies lay the dying soldiers. 罂粟地里躺着奄奄一息的士兵们。</li>
            </ul>
            <p>别的动词如属被动语态也可以倒装。</p>
            <ul class="disc">
                <li>In the distance could be seen the purple mountains. 远处可以见到紫色的山。</li>
            </ul>
            <p>主语如是代词则不能倒装：</p>
            <ul class="disc">
                <li>At the top of the hill it stood out against the sky. 它背衬青天矗立在山顶上。</li>
            </ul>
        </div>

        <div class="part">
            <h3>八. 否定副词之后的倒装</h3>
            <p>否定或近似否定副词(往往是时间或频度副词，如never, rarely, seldom)，或是具有否定作用的副词，如 little, on no account 等。如：<br>Never / Seldom has there been so much protest against the Bomb.&nbsp; 这么强烈的反对原子弹的抗议活动从未 / 很少 / 有过。</p>
            <ul class="disc">
                <li>Little does he realize how important this meeting is. 他对这个会议的重要性不甚了了。</li>
                <li>On no account must you accept any money if he offers it. 他如要给你钱，你可绝不能接受。</li>
            </ul>
            <p>当这种副词不在句首时，当然应该用正常词序：</p>
            <ul class="disc">
                <li>There has never / seldom been so much protest against the Bomb. 从未 / 很少发生过这么强烈的反对原子弹的抗议活动。</li>
                <li>He little realizes how important this meeting is. 他不甚明白这个会议的重要性。</li>
            </ul>
            <p>另外，有些含有否定意义的介词短语置于句首时，其后也要用倒装。如：</p>
            <ul class="disc">
                <li>In no case should you give up. 你绝不能放弃。</li>
                <li>On no condition should we tell her about it. 我们绝不能把此事告诉她。</li>
                <li>On no accounts must this switch be touched.这个开关是绝 不能触摸的。</li>
                <li>In [Under] no circumstances will I lend money to him.无论如何我也不会再借钱给他了。</li>
                <li>Under no circumstances should you lend him any money. 你无论如何都不该把钱借给他。</li>
                <li>No way will I go on working for that man. 我不再给那个人工作了。</li>
            </ul>
        </div>

        <div class="part">
            <h3>九. 涉及not only…but also…的部分倒装</h3>
            <p>not only…but also…前后连接两个句子时，not only后的句子要用部分倒装，但but also后的分句不用倒装。如：</p>
            <ul class="disc">
                <li>Not only did he come, but he saw her. 他不仅来了，而且还见到了她。</li>
                <li>Not only does she speak Spanish, (but) she also knows how to type. 她不但会说西班牙语，还会打字呢。</li>
                <li>Not only is he a teacher, but he is also a poet. 他不仅是一位教师，而且是一位诗人。</li>
                <li>Not only did he speak more correctly, but he spoke more easily. 不仅他讲得更正确，也讲得更不费劲了。</li>
                <li>Not only did they present a musical performance, but they also gave a brief introduction to the history of Western brass instruments. 他们不但做了音乐表演，而且简短地介绍了西方铜管乐器的历史。</li>
            </ul>
        </div>

        <div class="part">
            <h3>十. 类似So / Neither do I的部分倒装</h3>
            <p>so / neither / nor表示前面所说的情况也适合于后者时，其句型是：so / neither / nor+特殊词+主语。使句此句型需注意以下几点：<br>①内容是肯定时，用so，内容是否定时，用neither或nor；<br>②“特殊词”与前句中的情态动词、助动词或be相同，或根据前句的时态，用do, does, did；<br>③“特殊词”的人称和数与其后的主语一致；<br>④“特殊词”本身没有否定形式。如：</p>
            <ul class="disc">
                <li>You are young and so am I. 你年轻，我也年轻。</li>
                <li>She likes music and so do I. 她喜欢音乐，我也喜欢。</li>
                <li>If he can do it, so can I. 要是他能做此事，我也能。</li>
                <li>You aren’t young and neither am I. 你不年轻，我也不年轻。</li>
                <li>She hasn’t read it and nor have I. 她没有读它，我也没有读。</li>
            </ul>
        </div>

        <div class="part">
            <h3>十一. 涉及so…that的部分倒装</h3>
            <p>在so... that…结构中，若将so+adj. / adv. 置于句首，则其后的主句要用部分倒装。如：</p>
            <ul class="disc">
                <li>So cold was the weather that we had to stay at home. 天气太冷，我们只好呆在家里。</li>
                <li>So much did they eat that they could not move for the next hour. 他们吃得太多了，一个小时都动弹不得。</li>
                <li>So loudly did he speak that even people in the next room could hear him. 他讲话声音那么大，隔壁屋里的人都听得见。</li>
                <li>So fast does light travel that we can hardly imagine its speed. 光速很快，我们几乎没法像它的速度。</li>
                <li>So sudden was the attack that we had no time to escape. 袭击来得非常突然，我们来不及逃跑。</li>
            </ul>
            <p>类似地，当such…that…结构的such…置于句首时，such后的句子也要使用部分倒装。如：</p>
            <ul class="disc">
                <li>Such a nice man did he seem that we all believe him. 他像个很和蔼的人，所以我们都喜欢他。</li>
            </ul>
        </div>

        <div class="part">
            <h3>十二. 表语或状语置于句首时的完全倒装</h3>
            <p>有时为了强调或者为了使句子平衡或者为了使上下文连接得更加紧密，就将表语和地点状语(多为介词短语)置于句首，谓语动词也常置于主语前，构成完全倒装。如：</p>
            <ul class="disc">
                <li>By the door stood an armed guard. 门口站着一名手持武器的士兵。</li>
                <li>At the next table was a pretty girl waiting for someone. 隔壁桌上坐着一个等人的漂亮姑娘。</li>
                <li>Among these people was his friend Jim. 他的朋友吉姆就在这些人当中。</li>
                <li>By the window sat a young man with a magazine in his hand. 窗户边坐着一个年轻人，手里拿着一本杂志。</li>
                <li>At the front of the book is a table of contents, giving details of what is in the book. 书的前部有目录，详列了书中的内容。</li>
            </ul>
        </div>

        <div class="part">
            <h3>十三. as引导让步状语从句时的倒装</h3>
            <p>as作“虽然”解，引导让步状语从句时，必须将表语、状语或动词原形提到as前面。此时应注意几点：</p>
            <p>一是若提前的表语是没有形容词修饰的单数可数名词，要省略不定冠词；</p>
            <p>二是若提前的是动词原形(多为不及物动词)，与之连用的通常是may, might, will, would等，且这些词都要保留在原来的位置上(主语后)；</p>
            <p>三是though有时也可像as这样使作倒装。如：</p>
            <ul class="disc">
                <li>Tired as I was, I tried to help them. 虽然我很累，我还是努力帮助他们。</li>
                <li>Try as he would, he couldn’t open the door. 他试过多次了, 却仍打不开那门。</li>
                <li>Search as they would, they would find nobody in the house. 无论怎样搜查，他们在房子里仍然没有找到一个人。</li>
                <li>Hard as (though) they tried, they couldn’t make her change her mind. 尽管他们做了很大努力，却没法让她改变主意。</li>
                <li>Boy as he was, he behaved like a girl. 他虽是个男孩，但举止却像个女孩。</li>
            </ul>
        </div>

        <div class="part">
            <h3>十四. 非谓语动词置于句首的倒装</h3>
            <p>有时为了强调，可将谓语部分的现在分词、过去分词或不定式置于句首，从而构成倒装。如：</p>
            <ul class="disc">
                <li>Buried in the sands was an ancient village. 一个古老的村庄被埋在这沙土之中。</li>
                <li>Standing beside the table was his wife. 站在桌旁的是他的妻子。</li>
                <li>To be carefully considered are the following questions. 下列问题要仔细考虑。</li>
            </ul>
        </div>
    </div>

