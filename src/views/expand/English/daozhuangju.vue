            <h3>二、完全倒装的四种主要类型</h3>
            <ol>
                <li>
                    <b>here 和there位于句首时的倒装</b>
                    <p>表示地点的here和 there位于句首时，其后用完全倒装形式。这类倒装句的谓语通常是动词be和come, go等表示移动或动态的不及物动词：</p>
                    <ul class="disc">
                        <li>Here’s Tom. 汤姆在这里。</li>
                        <li>There’s Jim. 吉姆在那儿。</li>
                        <li>Here comes the bus. 公共汽车来了。</li>
                        <li>There goes the bell. 铃响了。</li>
                        <li>There goes the last train. 最后一班火车开走了。</li>
                    </ul>
                    <h6>【注意】</h6>
                    <ol class="style01">
                        <li>以上倒装句中的谓语动词come和go不能用进行时态，即不能说 Here is coming the bus。</li>
                        <li>
                            若主语为代词，则不倒装：
                            <p>Here I am. 我在这儿。/ 我来了。</p>
                            <p>Here it comes. 它来了。</p>
                        </li>
                        <li>
                            其中的动词有时也可能是stand, lie, live等表示状态的动词(表示存在)：
                            <p>There stood a desk against the wall. 靠墙放着一张书桌。</p>
                            <p>Once upon a time there lived a man known by the name of Beef. 从前有个人名叫比夫。</p>
                        </li>
                    </ol>

                </li>
                <li>
                    <b>away和down等位于句首时的倒装</b>
                    <p>地点副词away, down, in, off, out, over, round, up 等位于句首时，其后用完全倒装语序。这类倒装句的谓语通常表示动态的不及物动词：</p>
                    <ul class="disc">
                        <li>Away went the runners. 赛跑选手们跑远了。</li>
                        <li>Round and round flew the plane. 飞机盘旋着。</li>
                        <li>The door opened and in came Mr Smith. 门开了，史密斯先生进了来。</li>
                        <li>Down came the rain and up went the umbrellas. 下雨了，伞都撑起来了。</li>
                    </ul>
                    <h6>【注意】</h6>
                    <p>若主语为人称代词，则不能用倒装：</p>
                    <ul class="disc">
                        <li>Away he went. 他跑远了。</li>
                        <li>Down it came. 它掉了下来。</li>
                    </ul>
                </li>
                <li>
                    <b>状语或表语位于句首时的倒装</b>
                    <p>为了保持句子平衡或使上下文衔接紧密，有时可将状语或表语置于句首，句中主语和谓语完全倒装：</p>
                    <ul class="disc">
                        <li>Among these people was his friend Jim. 他的朋友吉姆就在这些人当中。</li>
                        <li>By the window sat a young man with a magazine in his hand. 窗户边坐着一个年轻人，手里拿着一本杂志。</li>
                    </ul>
                    <h6>【注意】</h6>
                    <p>在表语置于句首的这类倒装结构中，要注意其中的谓语应与其后的主语保持一致，而不是与位于句首的表语保持一致。比较：</p>
                    <ul class="disc">
                        <li>In the box was a cat. 箱子里是一只猫。</li>
                        <li>In the box were some cats. 箱子里是一些猫。</li>
                    </ul>
                </li>
                <li>
                    <b>分词和不定式置于句首的倒装</b>
                    <p>有时为了强调，可将谓语部分的现在分词、过去分词或不定式置于句首，从而构成倒装：</p>
                    <ul class="disc">
                        <li>Buried in the sands was an ancient village. 一个古老的村庄被埋在这沙土之中。</li>
                        <li>Standing beside the table was his wife. 站在桌旁的是他的妻子。</li>
                        <li>To be carefully considered are the following questions. 下列问题要仔细考虑。</li>
                    </ul>
                </li>
            </ol>
        </div>

