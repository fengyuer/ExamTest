            <h3>五. 倒装句中的主谓一致</h3>
            <p>在“副词here, there, now, then, up, down, in, out, away等、作状语或表语的介词短语或分词短语＋谓语动词＋主语”的倒装结构中，谓语动词的数由动词后面的主语决定。如：</p>
            <ul class="disc">
                <li>On the wall hang two maps. 墙上挂着两张地图。</li>
                <li>On the wall hangs a world of map. 墙上挂着一张世界地图。</li>
                <li>Here is your coat. 这是你的外套。</li>
                <li>Here are your running-shoes. 这是你的跑鞋。</li>
                <li>Such is the result. 结果就是这样。</li>
                <li>Such are the results. 这就是结果。</li>
            </ul>
        </div>

