        <div class="part">
            <h3>一、英语部分倒装用法归纳</h3>
            <ol>
                <li>
                    <b>否定副词位于句首时的倒装</b>
                    <p>在正式文体中，never, seldom, rarely, little, hardly, scarcely, no sooner, no longer, nowhere 等含有否定意义的副词若位于句首，则其后要用部分倒装：</p>
                    <ul class="disc">
                        <li>I shall never forgive him. / Never shall I forgive him. 我永远不会宽恕他。</li>
                        
                        <li>He seldom goes out for dinner. / Seldom does he go out for dinner. 他很少出去吃饭。</li>
                        
                        <li>She hardly has time to listen to music. / Hardly does she have time to listen to music. 她几乎没时间听音乐。</li>
                        
                        <li>He little realizes how important this meeting is. / Little does he realize how important this meeting is. 他不甚明白这个会议的重要性。</li>
                        
                        <li>We had no sooner reached the airport than the plane took off. / No sooner had we reached the airport than the plane took off. 我们刚到机场,飞机就起飞了。</li>
                    </ul>
                    <h6>【注意】</h6>
                    <ol class="style01">
                        <li>
                            对于not…until句型，当not until…位于句首时，其后的主句要用倒装语序：
                            <p>He didn’t leave the room until the rain stopped. / Not until the rain stopped did he leave the room. 雨停了之后他才离开这房间。</p>
                        </li>
                        <li>某些起副词作用的介词短语，由于含有否定词，若位于句首，其后要用部分倒装：
                            <p>On no accounts must this switch be touched. 这个开关是绝不能触摸的。</p>
                            <p>In [Under] no circumstances will I lend money to him.无论如何我也不会再借钱给他了。</p>
                        </li>
                        <li>
                                但是，in no time(立即，马上)位于句首时，其后无需用倒装语序：
                                <p>In no time he worked out the problem. 他马上就算出了那道题。</p>
                        </li>
                    </ol>
                </li>
                <li>
                    <b>“only+状语”位于句首时的倒装</b>
                    <p>当一个状语受副词only的修饰且置于句首时，其后用部分倒装语序：</p>
                    <ul class="disc">
                        <li>Only then did he realize that he was wrong. 到那时他才意识到他错了。</li>
                        <li>Only in this way are you able to do it well. 你只有用这种方法才能把它做好。</li>
                        <li>Only when he returned home did he realize what had happened. 当他回到家里时，才知道出了什么事。</li>
                    </ul>
                </li>
                <li>
                    <b>“so+adj. / adv.”位于句首时的倒装</b>
                    <p>副词so后接形容词或副词位于句首时，其后用部分倒装：</p>
                    <ul style="list-style-type:disc">
                        <li>So cold was the weather that we had to stay at home. 天气太冷，我们只好呆在家里。</li>
                        <li>So fast does light travel that we can hardly imagine its speed. 光速很快，我们几乎没法想像它的速度。</li>
                        <li>So sudden was the attack that we had no time to escape. 袭击来得非常突然，我们来不及逃跑。</li>
                    </ul>
                </li>
                <li>
                    <b>“so+助动词+主语”倒装</b>
                    <p>当要表示前面提出的某一肯定的情况也同样适合于后者，通常就要用“So+助动词+主语”这种倒装结构：</p>
                    <ul class="disc">
                        <li>You are young and so am I. 你年轻，我也年轻。</li>
                        <li>She likes music and so do I. 她喜欢音乐，我也喜欢。</li>
                        <li>If he can do it, so can I. 要是他能做此事，我也能。</li>
                    </ul>
                    <h6>【注意】</h6>
                    <ol class="style01">
                        <li>
                            若前面提出某一否定的情况，要表示后者也属于同样的否定情况，则应将其中的so改为neither或nor：
                            <p>You aren’t young and neither am I. 你不年轻，我也不年轻。</p>
                            <p>She hasn’t read it and nor have I. 她没有读它，我也没有读。</p>
                        </li>
                        <li>
                            注意该结构与表示强调或同意的“so+主语+特殊动词”结构的区别：
                            <p>"It was cold yesterday." "So it was." “昨天很冷。”“的确很冷。”</p>
                            <p>"Father, you promised." "Well, so I did." “爸爸，你答应过的。”“嗯，是答应过。”</p>
                        </li>
                    </ol>
                </li>
                <li>
                    <b>由not only…but also引出的倒装</b>
                    <p>当not only…but also位于句首引出句子时，not only 后的句子通常用部分倒装形式：</p>
                    <ul class="disc">
                        <li>Not only is he a teacher, but he is also a poet. 他不仅是一位教师，而且是一位诗人。</li>
                        <li>Not only did he speak more correctly, but he spoke more easily. 不仅他讲得更正确，也讲得更不费劲了。</li>
                    </ul>
                </li>
                <li>
                    <b>虚拟条件句的省略与倒装</b>
                    <p>当if引导的虚拟条件从句中含有had, were, should等时，如将if省略，则要将had, were, should等移到主语前，构成倒装句：</p>
                    <ul class="disc">
                        <li>Had you come yesterday, you would have seen him.若你昨天来，你就会见到他了。</li>
                        <li>Should you require anything give me a ring. 如果需要什么，可以给我打电话。</li>
                        <li>Were it not for your help, I would still be homeless. 要不是你帮助，我会仍然无家可归。</li>
                    </ul>
                    <h6>【注意】</h6>
                    <p>省略if后提前的had不一定是助动词：</p>
                    <p>Had I money, I would buy it. 假若我有钱，我就会买它。</p>
                </li>
            </ol>
        </div>

